public class StringUtils {

	/**
	 * The simple and beautiful delimiter for big amount of text.
	 */
	public static final String SMALL_DELIMITER = "\n#------------------------------------------------------------#\n";

	/**
	 * The simple and beautiful delimiter for big amount of text.
	 */
	public static final String BIG_DELIMITER_BELOW = "\n#************************************************************#" +
			SMALL_DELIMITER;

	/**
	 * The simple and beautiful delimiter for big amount of text.
	 */
	public static final String BIG_DELIMITER_ABOVE = SMALL_DELIMITER +
			"#************************************************************#\n";
}
